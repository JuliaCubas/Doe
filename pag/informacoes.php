<div id="requisitos" >
<br></br>
<br></br>
<p class="titulo">Quais são os requisitos para doação de sangue?</p>
<br></br>

<p class="texto">Pessoas entre 16 e 69 podem realizar a doação, porém, os menores devem preferencialmente estar acompanhados do responsável legal, tendo os documentos necessários em mãos, e o formulário de autorização preenchido devidamente</p>
<p class="texto">Estar alimentado. Evite alimentos gordurosos nas 03 horas que antecedem a doação de sangue.</p>
<p class="texto">Caso seja após o almoço, aguardar 02 horas.</p>
<p class="texto">Ter dormido pelo menos 6 horas nas últimas 24 horas.</p>
<p class="texto">Pessoas com idade entre 60 e 69 anos só poderão doar sangue se já o tiverem feito antes dos 60 anos.</p>
<p class="texto">A frequência máxima é de quatro doações de sangue anuais para o homem e de três doações de sangue anuais para as mulher.</p>
<p class="texto">O intervalo mínimo entre uma doação de sangue e outra é de dois meses para os homens e de três meses para as mulheres.</p>
