<br></br>
<p class="titulo">Situação atual da doação no país</p>
<br></br>

<p class="texto">Atualmente, segundo dados do Ministério da Saúde divulgados pela Agência Brasil, a taxa de doação da população brasileira é de 1,6% número que está dentro do preconizado pela OMS (Organização Mundial da Saúde). Em 2019, o governo investiu R$ 1,5 bilhão na rede de sangue e hemoderivados no Brasil, e R$ 1,6 bilhão em 2020, tal valor diz respeito à aquisição de medicamentos, equipamentos, reformas, ampliação e qualificação da rede. Devido à pandemia COVID-19, os estoques de sangue diminuíram consideravelmente, houve uma queda de aproximadamente 20% no volume de doações no país, consequentemente alguns hemocentros ficaram em situações críticas, obtendo uma drástica perda de 75% no volume de estoque nos bancos de sangue.</p>
<p class="texto">Dentre a população brasileira, 16 entre mil habitantes são doadores de sangue, considerando esse número de doadores, em 2018 estipulou-se que 60% eram do sexo masculino, e 40% do sexo feminino, e cerca de 63% são maiores de 29 anos. Quanto à motivação, 46,7% afirmam que doam para reposição, ou seja, o doador tem algum vínculo direto com o paciente que irá receber o sangue, entretanto, outros 53,2% correspondem à doação espontânea, que se trata do ato voluntário, doações realizadas com o intuito altruísta de manter a situação dos bancos de sangue estáveis, e dessa forma auxiliar os pacientes que necessitam.</p>

