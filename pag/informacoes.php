<div id="historia">
<p  class="titulo">História de doação de sangue</p>

<br></br>
<p class="texto">A história da transfusão de sangue é dívida em três partes pré-histórica com o médico britânico Willian Harvey, pré-científico de 1616 após a descoberta da circulação de sangue, e por fim o terceiro científico, com o cientista Landsteiner.</p>
<p class="texto">Com a sua descoberta os primeiros processos eram realizados de braço a braço, por falta de anticoagulantes que não permitiam a estocagem do sangue em recipientes. Só durante as guerras mundiais foi descoberto uma solução a base de citrato de sódio.</p>
<p class="texto">Em 1667, Jean Baptiste Denis começou a realizar experimentos de transfusão usando um tubo de prata, mas o que não esperávamos é de esses primeiros testes foram realizados com sangue de cabra, que segundo Jean o sangue de animais estaria menos contaminado com vícios e paixões. Depois de três transfusões ele veio a falência. </p>
<p class="texto">Por decorrência das falhas nos testes de transfusões heteróloga, sangue de animais diferentes, a faculdade de Paris, Roma e Royal Society da Inglaterra proibiram tal procedimento.</p>
<p class="texto">Mesmo proibida, em 1788, os cientistas Potick e Landois tiveram bons resultados, usando da técnica homologas, ou seja, animais de mesma espécie. Concluindo logo que este procedimento poderia ser muito benéfico e até mesmo salvar vidas.</p>
<p class="texto">O imunologista Karl Landsteiner descobriu que o soro do sangue se misturado com outra pessoa se coagula e assim descobrindo uma das maiores descobertas, o sistema de sangue ABO.</p>
<br></br>
<p class="texto">Além do sistema de grupo ABO, foi descoberto ao realizar um teste onde injetavam sangue de macaco no corpo de cobaias, e o corpo produzia anticorpos, e essa substância passou a ser chamada de anti-Rh, já que no sangue do macaco havia um antígeno Rh. Esse grupo foi divido em dois subgrupos, Rh positivo, onde 85% das pessoas possuem o Rh nas hemácias, e Rh negativo onde 15% não possuem o Rh. E caso o Rh do sangue dos corpos não fossem compatíveis, o Rh negativo poderia enfatizar uma sensibilidade dos anticorpos Rh.</p>
<p class="texto">No Brasil o ato de doar sangue era remunerado, desta maneira o banco de sangue do Brasil teve uma alta no número de doadores, porém, o sangue coletado e remunerado era enviado a bancos de sangues privado dificultando a fiscalização do mesmo, causando ao governo uma reação contrária. </p>
<p class="texto">O surgimento da hemoterapia no Brasil,  se deve como questão de política pública e interesse social foi motivada pela contestação do sistema de saúde vigente, em razão do aumento da contaminação sanguínea, uma vez que as doenças transfusionais estavam vinculadas às doações remuneradas (PIMENTEL, 2006). O impacto da Aids, o “medo do sangue”, e o olhar para a confiabilidade dos serviços contribuíram para a politização da opinião pública e para o surgimento de diversos movimentos sociais em torno de reformas sanitárias, de novas propostas para a política de saúde, para a segurança transfusional. Então, na década de 80 ocorre no país o surgimento da política pública do sangue, com implantação de uma rede de hemocentros e o enfoque na doação voluntária e não remunerada, como ato de solidariedade e altruísmo.</p>
</div>


