<div id="tipos">
<br></br>
<br></br>
<p class="titulo">Tipos de sangue</p>
<br></br>
<p class="texto">Com base em seus experimentos do século XIX à virada do século XX, o médico austríaco Landsteiner descreveu três tipos de sangue, chamados A, B e O, que resultaram na famosa classificação de grupo sanguíneo ABO. Essa descoberta lhe rendeu o Prêmio Nobel de Medicina em 1930. Dois anos depois, foi determinado o quarto tipo de sangue: o tipo AB, formando assim os 4 tipos de sangue usados ​​atualmente no sistema ABO. Nos dez anos seguintes, ele descobriu a existência do chamado fator Rh, mesmo respeitando o sistema ABO, ele também é a causa de certas incompatibilidades de tipo sanguíneo. Com base nesse achado, os indivíduos são classificados como Rh-positivos ou Rh-negativos de acordo com a presença de fatores Rh no sangue.	</p>
<p class="texto">Os glóbulos vermelhos (eritrócitos ou hemácias) têm uma identidade que permite a classificação do sangue em A, B, AB e O. No Brasil, os grupos mais comuns são o O e o A, que abrangem 87% da população. O B responde por 10% e o AB, por apenas 3%. Assim, o sangue pode ser classificado nesses quatro tipos de acordo com o sistema ABO.  Atualmente, as transfusões sanguíneas utilizam as classificações ABO e Rh para evitar que um sangue incompatível seja administrado em um paciente que necessita de transfusão. Sendo assim, são 8 os tipos sanguíneos:
</p>
<ul>
<li>A+ (grupo sanguíneo A com fator Rh positivo).</li>
<li>B+ (grupo sanguíneo B com fator Rh positivo).</li>
<li>AB+ (grupo sanguíneo AB com fator Rh positivo).
</li>
<li>O+ (grupo sanguíneo O com fator Rh positivo).
</li>
<li>A- (grupo sanguíneo A com fator Rh negativo).</li>
<li>B- (grupo sanguíneo B com fator Rh negativo).</li>
<li>AB- (grupo sanguíneo AB com fator Rh negativo).
</li>
<li>O- (grupo sanguíneo O com fator Rh negativo).</li>
</ul>
<p class="texto">Se você já se perguntou “como descobrir meu tipo sanguíneo?”, então esse tópico é para você! Os doadores de sangue precisam realizar exames sanguíneos. Ou seja, ao doar sangue, você consegue descobrir qual o seu tipo de sangue de graça.</p>

