<p class="texto">Olá, somos alunas da Etec Bartolomeu Bueno da Silva e viemos hoje apresentar o DOE+.</p>
<p class="texto">Nosso público-alvo é destinado a todas as pessoas entre 16 e 60 anos que desejam doar sangue. Que muitas das vezes acabam não realizando o procedimento ou por falta de conhecimento e informações ou até mesmo o medo.</p>
<p class="texto">Devido à pandemia COVID-19, os estoques de sangue diminuíram consideravelmente, houve uma queda de aproximadamente 20% no volume de doações no país, consequentemente alguns hemocentros ficaram em situações críticas, obtendo uma drástica perda de 75% no volume de estoque nos bancos de sangue.</p>
<p class="texto">Nosso projeto foi criado através de pesquisas em artigos encontrados no Google acadêmico e da própria plataforma da pró-sangue.</p>
<p class="texto">Planejamos criar uma plataforma interativa, com sistema de publicações de comentários para estimular cada vez mais pessoas a doar sangue, mapas para localizar os hemocentros mais próximos da região do doador, além de informações sobre o procedimento.</p>
<p class="texto">O serviço de transfusão de sangue é um procedimento relativamente simples, porém, que é capaz de salvar vidas, já que pode ser utilizado em diversas situações, incluindo, por exemplo, no tratamento de doenças como a leucemia. O sangue no corpo humano tem como função básica a transportação de substâncias, sendo vital para nossa existência. Além de ser um ato altruísta, aquele que é fidelizado como doador recebe benefícios consideráveis, como a precedência em agendamentos de exames e atendimento em consultas médicas e odontológicas. 
Sendo assim, esse projeto tem como objetivo aprimorar as estratégias de marketing voltadas a transfusão de sangue no país, ampliando o alcance de informações sobre o procedimento dentre a comunidade através da disponibilização de maneira simplificada no website.</p>
